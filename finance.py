import sys
import io
if isinstance(sys.stdout, io.TextIOWrapper):
    sys.stdout.reconfigure(encoding='utf-8')

import json
import os
from datetime import datetime

# ชื่อไฟล์ที่ใช้เก็บข้อมูล
DATA_FILE = "finance_data.json"


def โหลดข้อมูล():
    # ถ้ายังไม่มีไฟล์ให้คืนค่าเริ่มต้นเป็น list ว่าง
    if not os.path.exists(DATA_FILE):
        return []
    # เปิดไฟล์และแปลง JSON เป็น list
    with open(DATA_FILE, "r", encoding="utf-8") as f:
        return json.load(f)


def บันทึกข้อมูล(รายการ):
    # เขียน list ทั้งหมดลงไฟล์ JSON ให้อ่านง่าย
    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump(รายการ, f, ensure_ascii=False, indent=2)


def เพิ่มรายรับ(คำอธิบาย, จำนวนเงิน):
    # โหลดข้อมูลเดิมก่อนเพิ่มรายการใหม่
    รายการ = โหลดข้อมูล()
    # สร้าง dict รายการรับพร้อมวันเวลาปัจจุบัน
    รายการใหม่ = {
        "ประเภท": "รายรับ",
        "คำอธิบาย": คำอธิบาย,
        "จำนวนเงิน": จำนวนเงิน,
        "วันที่": datetime.now().strftime("%Y-%m-%d %H:%M"),
    }
    รายการ.append(รายการใหม่)
    บันทึกข้อมูล(รายการ)


def เพิ่มรายจ่าย(คำอธิบาย, จำนวนเงิน):
    # โหลดข้อมูลเดิมก่อนเพิ่มรายการใหม่
    รายการ = โหลดข้อมูล()
    # สร้าง dict รายการจ่ายพร้อมวันเวลาปัจจุบัน
    รายการใหม่ = {
        "ประเภท": "รายจ่าย",
        "คำอธิบาย": คำอธิบาย,
        "จำนวนเงิน": จำนวนเงิน,
        "วันที่": datetime.now().strftime("%Y-%m-%d %H:%M"),
    }
    รายการ.append(รายการใหม่)
    บันทึกข้อมูล(รายการ)


def ดูรายการทั้งหมด():
    # โหลดรายการทั้งหมดแล้วคืนค่ากลับไปให้เมนูแสดงผล
    return โหลดข้อมูล()


def ดูรายการตามประเภท(ประเภท):
    # กรองเฉพาะรายการที่ตรงกับประเภทที่ระบุ ("รายรับ" หรือ "รายจ่าย")
    รายการทั้งหมด = โหลดข้อมูล()
    return [r for r in รายการทั้งหมด if r["ประเภท"] == ประเภท]


def คำนวณยอดคงเหลือ():
    รายการ = โหลดข้อมูล()
    # รวมรายรับทั้งหมด
    รายรับรวม = sum(r["จำนวนเงิน"] for r in รายการ if r["ประเภท"] == "รายรับ")
    # รวมรายจ่ายทั้งหมด
    รายจ่ายรวม = sum(r["จำนวนเงิน"] for r in รายการ if r["ประเภท"] == "รายจ่าย")
    # ยอดคงเหลือ = รายรับ - รายจ่าย
    ยอดคงเหลือ = รายรับรวม - รายจ่ายรวม
    return รายรับรวม, รายจ่ายรวม, ยอดคงเหลือ
