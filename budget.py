def หน้าดูรายการแยกประเภท():
    # ถามผู้ใช้ว่าต้องการดูประเภทใด
    print("\n--- ดูรายการแยกประเภท ---")
    print("  1. รายรับ")
    print("  2. รายจ่าย")
    ตัวเลือก = input("  เลือก (1-2): ").strip()
