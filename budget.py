from finance import เพิ่มรายรับ, เพิ่มรายจ่าย, ดูรายการทั้งหมด, คำนวณยอดคงเหลือ


def รับจำนวนเงิน(prompt):
    # วนถามซ้ำจนกว่าจะได้ตัวเลขที่มากกว่า 0
    while True:
        try:
            จำนวน = float(input(prompt))
            if จำนวน <= 0:
                print("  กรุณาใส่จำนวนเงินที่มากกว่า 0")
                continue
            return จำนวน
        except ValueError:
            # ดักจับกรณีผู้ใช้พิมพ์ข้อความที่ไม่ใช่ตัวเลข
            print("  กรุณาใส่ตัวเลขเท่านั้น")


def หน้าเพิ่มรายรับ():
    print("\n--- เพิ่มรายรับ ---")
    คำอธิบาย = input("  รายละเอียด (เช่น เงินเดือน): ").strip()
    if not คำอธิบาย:
        # ถ้าไม่ใส่คำอธิบายให้ใช้ค่าเริ่มต้น
        คำอธิบาย = "รายรับ"
    จำนวน = รับจำนวนเงิน("  จำนวนเงิน (บาท): ")
    เพิ่มรายรับ(คำอธิบาย, จำนวน)
    print(f"  บันทึกรายรับ '{คำอธิบาย}' {จำนวน:,.2f} บาท เรียบร้อย")


def หน้าเพิ่มรายจ่าย():
    print("\n--- เพิ่มรายจ่าย ---")
    คำอธิบาย = input("  รายละเอียด (เช่น ค่าอาหาร): ").strip()
    if not คำอธิบาย:
        # ถ้าไม่ใส่คำอธิบายให้ใช้ค่าเริ่มต้น
        คำอธิบาย = "รายจ่าย"
    จำนวน = รับจำนวนเงิน("  จำนวนเงิน (บาท): ")
    เพิ่มรายจ่าย(คำอธิบาย, จำนวน)
    print(f"  บันทึกรายจ่าย '{คำอธิบาย}' {จำนวน:,.2f} บาท เรียบร้อย")


def หน้าดูรายการ():
    print("\n--- รายการทั้งหมด ---")
    รายการ = ดูรายการทั้งหมด()
    # ตรวจสอบว่ามีข้อมูลหรือยัง
    if not รายการ:
        print("  ยังไม่มีรายการใดๆ")
        return
    # แสดงทุกรายการพร้อมหมายเลขลำดับ
    for i, r in enumerate(รายการ, 1):
        สัญลักษณ์ = "+" if r["ประเภท"] == "รายรับ" else "-"
        print(f"  {i:3}. [{r['วันที่']}] {r['ประเภท']}  {สัญลักษณ์}{r['จำนวนเงิน']:>10,.2f} บาท  — {r['คำอธิบาย']}")


def หน้าดูยอดคงเหลือ():
    print("\n--- ยอดคงเหลือ ---")
    รายรับรวม, รายจ่ายรวม, ยอดคงเหลือ = คำนวณยอดคงเหลือ()
    print(f"  รายรับรวม :  {รายรับรวม:>12,.2f} บาท")
    print(f"  รายจ่ายรวม:  {รายจ่ายรวม:>12,.2f} บาท")
    print(f"  {'─' * 28}")
    # แสดงสีต่างกันตามว่าติดลบหรือไม่ (ใช้ข้อความแทนเพราะ terminal ไม่รองรับสีทุกที่)
    สถานะ = "คงเหลือ" if ยอดคงเหลือ >= 0 else "ขาดเงิน"
    print(f"  {สถานะ}    :  {ยอดคงเหลือ:>12,.2f} บาท")


def แสดงเมนู():
    # แสดงตัวเลือกเมนูหลัก
    print("\n=============================")
    print("   บันทึกรายรับ-รายจ่าย")
    print("=============================")
    print("  1. เพิ่มรายรับ")
    print("  2. เพิ่มรายจ่าย")
    print("  3. ดูรายการทั้งหมด")
    print("  4. ดูยอดคงเหลือ")
    print("  5. ออก")
    print("-----------------------------")


def main():
    # วนลูปแสดงเมนูจนกว่าผู้ใช้จะเลือกออก
    while True:
        แสดงเมนู()
        ตัวเลือก = input("  เลือก (1-5): ").strip()

        if ตัวเลือก == "1":
            หน้าเพิ่มรายรับ()
        elif ตัวเลือก == "2":
            หน้าเพิ่มรายจ่าย()
        elif ตัวเลือก == "3":
            หน้าดูรายการ()
        elif ตัวเลือก == "4":
            หน้าดูยอดคงเหลือ()
        elif ตัวเลือก == "5":
            # ออกจากโปรแกรม
            print("\n  ขอบคุณที่ใช้งาน บ๊ายบาย!")
            break
        else:
            # ดักจับกรณีผู้ใช้พิมพ์ตัวเลือกที่ไม่มีในเมนู
            print("  กรุณาเลือก 1-5 เท่านั้น")


# เริ่มโปรแกรมเมื่อรันไฟล์นี้โดยตรง
if __name__ == "__main__":
    main()
