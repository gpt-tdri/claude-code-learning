import sys
sys.stdout.reconfigure(encoding='utf-8')

# โปรแกรมคำนวณเกรดเฉลี่ย (GPA) จากคะแนนหลายวิชา

def คำนวณเกรด(คะแนน):
    if คะแนน >= 80:
        return "A"
    elif คะแนน >= 70:
        return "B"
    elif คะแนน >= 60:
        return "C"
    elif คะแนน >= 50:
        return "D"
    else:
        return "F"

def สรุปผล(รายการคะแนน):
    # คำนวณค่าเฉลี่ย
    เฉลี่ย = sum(รายการคะแนน) / len(รายการคะแนน)

    # นับจำนวนวิชาที่ผ่าน (คะแนน >= 50)
    ผ่าน = 0
    for คะแนน in รายการคะแนน:
        if คะแนน >= 50:
            ผ่าน = ผ่าน + 1  # แก้แล้ว: ใช้ = แทน ==

    print(f"คะแนนเฉลี่ย: {เฉลี่ย:.1f}")
    print(f"เกรด: {คำนวณเกรด(เฉลี่ย)}")
    print(f"ผ่าน {ผ่าน} วิชา จากทั้งหมด {len(รายการคะแนน)} วิชา")

# ทดสอบด้วยคะแนน 5 วิชา
คะแนนทั้งหมด = [85, 72, 55, 90, 48]
สรุปผล(คะแนนทั้งหมด)
