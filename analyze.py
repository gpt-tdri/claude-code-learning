import sys
import io
if isinstance(sys.stdout, io.TextIOWrapper):
    sys.stdout.reconfigure(encoding='utf-8')

import csv

# อ่านข้อมูลจากไฟล์ CSV แล้วเก็บเป็น list ของ dict
นักเรียน = []
with open("students.csv", encoding="utf-8") as f:
    for แถว in csv.DictReader(f):
        # แปลงคะแนนจาก string เป็น int
        นักเรียน.append({
            "ชื่อ":           แถว["ชื่อ"],
            "อายุ":           int(แถว["อายุ"]),
            "คะแนนคณิต":     int(แถว["คะแนนคณิต"]),
            "คะแนนภาษาไทย":  int(แถว["คะแนนภาษาไทย"]),
            "คะแนนวิทย์":    int(แถว["คะแนนวิทย์"]),
        })

# คำนวณคะแนนรวมของนักเรียนแต่ละคน
for น in นักเรียน:
    น["คะแนนรวม"] = น["คะแนนคณิต"] + น["คะแนนภาษาไทย"] + น["คะแนนวิทย์"]

จำนวน = len(นักเรียน)

# หาค่าเฉลี่ยแต่ละวิชา
เฉลี่ยคณิต  = sum(น["คะแนนคณิต"]    for น in นักเรียน) / จำนวน
เฉลี่ยไทย   = sum(น["คะแนนภาษาไทย"] for น in นักเรียน) / จำนวน
เฉลี่ยวิทย์ = sum(น["คะแนนวิทย์"]   for น in นักเรียน) / จำนวน

# หานักเรียนที่มีคะแนนรวมสูงสุดและต่ำสุด
อันดับหนึ่ง = max(นักเรียน, key=lambda น: น["คะแนนรวม"])
อันดับสุดท้าย = min(นักเรียน, key=lambda น: น["คะแนนรวม"])

# ─── แสดงผล ───────────────────────────────────────────
print("=" * 44)
print("       ผลวิเคราะห์คะแนนนักเรียน")
print("=" * 44)

print(f"\n  จำนวนนักเรียนทั้งหมด : {จำนวน} คน")

print("\n  คะแนนเฉลี่ยแต่ละวิชา")
print(f"  {'─' * 30}")
print(f"  คณิตศาสตร์  : {เฉลี่ยคณิต:6.1f} คะแนน")
print(f"  ภาษาไทย     : {เฉลี่ยไทย:6.1f} คะแนน")
print(f"  วิทยาศาสตร์ : {เฉลี่ยวิทย์:6.1f} คะแนน")

print("\n  นักเรียนคะแนนรวมสูงสุด")
print(f"  {'─' * 30}")
print(f"  ชื่อ      : {อันดับหนึ่ง['ชื่อ']}")
print(f"  คะแนนรวม : {อันดับหนึ่ง['คะแนนรวม']} / 300 คะแนน")
print(f"  (คณิต {อันดับหนึ่ง['คะแนนคณิต']}  ไทย {อันดับหนึ่ง['คะแนนภาษาไทย']}  วิทย์ {อันดับหนึ่ง['คะแนนวิทย์']})")

print("\n  นักเรียนคะแนนรวมต่ำสุด")
print(f"  {'─' * 30}")
print(f"  ชื่อ      : {อันดับสุดท้าย['ชื่อ']}")
print(f"  คะแนนรวม : {อันดับสุดท้าย['คะแนนรวม']} / 300 คะแนน")
print(f"  (คณิต {อันดับสุดท้าย['คะแนนคณิต']}  ไทย {อันดับสุดท้าย['คะแนนภาษาไทย']}  วิทย์ {อันดับสุดท้าย['คะแนนวิทย์']})")

print("\n" + "=" * 44)
