import sys
import io
if isinstance(sys.stdout, io.TextIOWrapper):
    sys.stdout.reconfigure(encoding='utf-8')

import csv

# อ่านข้อมูลจากไฟล์ CSV แล้วเก็บเป็น list ของ dict
try:
    นักเรียน = []
    with open("students.csv", encoding="utf-8") as f:
        for แถว in csv.DictReader(f):
            try:
                # แปลงคะแนนจาก string เป็น int — ถ้าข้อมูลไม่ใช่ตัวเลขให้ข้ามแถวนั้น
                นักเรียน.append({
                    "ชื่อ":           แถว["ชื่อ"],
                    "อายุ":           int(แถว["อายุ"]),
                    "คะแนนคณิต":     int(แถว["คะแนนคณิต"]),
                    "คะแนนภาษาไทย":  int(แถว["คะแนนภาษาไทย"]),
                    "คะแนนวิทย์":    int(แถว["คะแนนวิทย์"]),
                })
            except (ValueError, KeyError) as e:
                print(f"  คำเตือน: ข้ามแถว '{แถว.get('ชื่อ', '?')}' — ข้อมูลไม่ถูกต้อง ({e})")
except FileNotFoundError:
    print("ไม่พบไฟล์ students.csv — กรุณาตรวจสอบว่าไฟล์อยู่ในโฟลเดอร์เดียวกัน")
    sys.exit(1)

if not นักเรียน:
    print("ไม่มีข้อมูลนักเรียนที่ถูกต้อง ไม่สามารถวิเคราะห์ได้")
    sys.exit(1)

# คำนวณคะแนนรวมของนักเรียนแต่ละคน
for น in นักเรียน:
    น["คะแนนรวม"] = น["คะแนนคณิต"] + น["คะแนนภาษาไทย"] + น["คะแนนวิทย์"]

จำนวน = len(นักเรียน)

# หาค่าเฉลี่ยแต่ละวิชา
เฉลี่ยคณิต  = sum(น["คะแนนคณิต"]    for น in นักเรียน) / จำนวน
เฉลี่ยไทย   = sum(น["คะแนนภาษาไทย"] for น in นักเรียน) / จำนวน
เฉลี่ยวิทย์ = sum(น["คะแนนวิทย์"]   for น in นักเรียน) / จำนวน

# หานักเรียนที่มีคะแนนรวมสูงสุดและต่ำสุด
อันดับหนึ่ง = max(นักเรียน, key=lambda น: น["คะแนนรวม"])
อันดับสุดท้าย = min(นักเรียน, key=lambda น: น["คะแนนรวม"])

# ─── เตรียมข้อความผลลัพธ์ทั้งหมดเป็น list ───────────────
