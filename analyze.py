import sys
import io
if isinstance(sys.stdout, io.TextIOWrapper):
    sys.stdout.reconfigure(encoding='utf-8')

import csv

# อ่านข้อมูลจากไฟล์ CSV แล้วเก็บเป็น list ของ dict
นักเรียน = []
with open("students.csv", encoding="utf-8") as f:
    for แถว in csv.DictReader(f):
        # แปลงคะแนนจาก string เป็น int
        นักเรียน.append({
            "ชื่อ":           แถว["ชื่อ"],
            "อายุ":           int(แถว["อายุ"]),
            "คะแนนคณิต":     int(แถว["คะแนนคณิต"]),
            "คะแนนภาษาไทย":  int(แถว["คะแนนภาษาไทย"]),
            "คะแนนวิทย์":    int(แถว["คะแนนวิทย์"]),
        })

# คำนวณคะแนนรวมของนักเรียนแต่ละคน
for น in นักเรียน:
    น["คะแนนรวม"] = น["คะแนนคณิต"] + น["คะแนนภาษาไทย"] + น["คะแนนวิทย์"]

จำนวน = len(นักเรียน)

# หาค่าเฉลี่ยแต่ละวิชา
เฉลี่ยคณิต  = sum(น["คะแนนคณิต"]    for น in นักเรียน) / จำนวน
เฉลี่ยไทย   = sum(น["คะแนนภาษาไทย"] for น in นักเรียน) / จำนวน
เฉลี่ยวิทย์ = sum(น["คะแนนวิทย์"]   for น in นักเรียน) / จำนวน

# หานักเรียนที่มีคะแนนรวมสูงสุดและต่ำสุด
อันดับหนึ่ง = max(นักเรียน, key=lambda น: น["คะแนนรวม"])
อันดับสุดท้าย = min(นักเรียน, key=lambda น: น["คะแนนรวม"])

# ─── เตรียมข้อความผลลัพธ์ทั้งหมดเป็น list ───────────────
บรรทัด = [
    "=" * 44,
    "       ผลวิเคราะห์คะแนนนักเรียน",
    "=" * 44,
    "",
    f"  จำนวนนักเรียนทั้งหมด : {จำนวน} คน",
    "",
    "  คะแนนเฉลี่ยแต่ละวิชา",
    f"  {'─' * 30}",
    f"  คณิตศาสตร์  : {เฉลี่ยคณิต:6.1f} คะแนน",
    f"  ภาษาไทย     : {เฉลี่ยไทย:6.1f} คะแนน",
    f"  วิทยาศาสตร์ : {เฉลี่ยวิทย์:6.1f} คะแนน",
    "",
    "  นักเรียนคะแนนรวมสูงสุด",
    f"  {'─' * 30}",
    f"  ชื่อ      : {อันดับหนึ่ง['ชื่อ']}",
    f"  คะแนนรวม : {อันดับหนึ่ง['คะแนนรวม']} / 300 คะแนน",
    f"  (คณิต {อันดับหนึ่ง['คะแนนคณิต']}  ไทย {อันดับหนึ่ง['คะแนนภาษาไทย']}  วิทย์ {อันดับหนึ่ง['คะแนนวิทย์']})",
    "",
    "  นักเรียนคะแนนรวมต่ำสุด",
    f"  {'─' * 30}",
    f"  ชื่อ      : {อันดับสุดท้าย['ชื่อ']}",
    f"  คะแนนรวม : {อันดับสุดท้าย['คะแนนรวม']} / 300 คะแนน",
    f"  (คณิต {อันดับสุดท้าย['คะแนนคณิต']}  ไทย {อันดับสุดท้าย['คะแนนภาษาไทย']}  วิทย์ {อันดับสุดท้าย['คะแนนวิทย์']})",
    "",
    "=" * 44,
]

# พิมพ์ออกหน้าจอและบันทึกลงไฟล์พร้อมกันในลูปเดียว
with open("report.txt", "w", encoding="utf-8") as f:
    for บรรทัดนี้ in บรรทัด:
        print(บรรทัดนี้)
        f.write(บรรทัดนี้ + "\n")

print("\n  บันทึกผลลงไฟล์ report.txt เรียบร้อย")
