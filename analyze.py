    f"  ภาษาไทย     : {เฉลี่ยไทย:6.1f} คะแนน",
    f"  วิทยาศาสตร์ : {เฉลี่ยวิทย์:6.1f} คะแนน",
    "",
    "  นักเรียนคะแนนรวมสูงสุด",
    f"  {'─' * 30}",
    f"  ชื่อ      : {อันดับหนึ่ง['ชื่อ']}",
    f"  คะแนนรวม : {อันดับหนึ่ง['คะแนนรวม']} / 300 คะแนน",
    f"  (คณิต {อันดับหนึ่ง['คะแนนคณิต']}  ไทย {อันดับหนึ่ง['คะแนนภาษาไทย']}  วิทย์ {อันดับหนึ่ง['คะแนนวิทย์']})",
